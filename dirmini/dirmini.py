def dirbuster(url, wordlist):
    print(url + "dirmini")
    print("=========================================")

    print("\n")
    results = []
    for directory in wordlist:
        response = requests.get(f"{url}/{directory}")

        if response.status_code == 200:
            results.append((directory, "200"))
        else:
            results.append((directory, "Error"))

    return results

